"""По данному целому неотрицательному n вычислите значение !n...N! = 1*2*3...* N (произведение чисел от 1 до N) 0! = 1.
Решить задачу используя цикл while.

РЕШЕНИЕ ЗАДАЧИ ВТОРЫМ СПОСОБОМ"""

n = int(input("Введите целое неотрицательное число: "))
i = 1
while n > 1:
    i *= n
    n -= 1
print(f"Факториал числа равен: {i}")
